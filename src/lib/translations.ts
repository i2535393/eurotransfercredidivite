export function getTranslation(lang: string, key: string, params?: Record<string, string>): string {
  const normLang = (lang || "Français").toLowerCase().trim();
  
  // Find key match
  let matchedLang = "français";
  if (normLang.includes("eng") || normLang === "en") {
    matchedLang = "english";
  } else if (normLang.includes("deu") || normLang.includes("ger") || normLang === "de") {
    matchedLang = "deutsch";
  } else if (normLang.includes("esp") || normLang === "es") {
    matchedLang = "español";
  } else if (normLang.includes("por") || normLang === "pt") {
    matchedLang = "português";
  } else if (normLang.includes("ita") || normLang === "it") {
    matchedLang = "italiano";
  } else if (normLang.includes("mag") || normLang.includes("hung") || normLang === "hu") {
    matchedLang = "magyar";
  } else if (normLang.includes("rom") || normLang === "ro") {
    matchedLang = "română";
  } else if (normLang.includes("pol") || normLang === "pl") {
    matchedLang = "polski";
  } else if (normLang.includes("tür") || normLang.includes("tur") || normLang === "tr") {
    matchedLang = "türkçe";
  } else if (normLang.includes("ned") || normLang.includes("belg") || normLang === "nl") {
    matchedLang = "nederlands";
  } else if (normLang.includes("rus") || normLang === "ru") {
    matchedLang = "russkiy";
  }

  const dict = translations[matchedLang] || translations["français"];
  let text = (dict as any)[key] || (translations["français"] as any)[key] || key;

  if (params) {
    Object.entries(params).forEach(([k, v]) => {
      text = text.replace(`{${k}}`, v);
    });
  }

  return text;
}
